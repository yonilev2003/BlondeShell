#!/usr/bin/env node

import 'dotenv/config.js';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { execSync } from 'child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.join(__dirname, '../../');

// Parse CLI args
const args = process.argv.slice(2);
const expFilter = args.find(a => a.startsWith('--exp'))?.split('=')[1];

const configPath = path.join(projectRoot, 'experiments/config.json');
const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));

const experiments = expFilter
  ? config.experiments.filter(e => e.id === expFilter)
  : config.experiments;

if (experiments.length === 0) {
  console.error(`Error: No experiments matching ${expFilter}`);
  process.exit(1);
}

console.log('\n‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó');
console.log('‚ïë  BlondeShell Image Pipeline ‚Äî Batch Experiment Runner   ‚ïë');
console.log('‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù\n');

for (const exp of experiments) {
  console.log(`\nüìä ${exp.name} (${exp.total_images} images)\n`);

  for (const variant of exp.variants) {
    try {
      const cmd = `node scripts/experiments/generate_exp.js --exp=${exp.id} --variant=${variant}`;
      execSync(cmd, {
        cwd: projectRoot,
        stdio: 'inherit',
        env: process.env
      });
    } catch (error) {
      console.error(`\n‚ùå Failed: ${exp.id}/${variant}`);
      process.exit(1);
    }
  }

  console.log(`\n‚úÖ Completed ${exp.id}\n`);
}

console.log('\n‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
console.log('‚úÖ All experiments complete');
console.log('\nNext: node scripts/experiments/score_exp.js\n');
